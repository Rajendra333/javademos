package pratice;

public class Switchcase {
	public static void main(String[] args) {
		int marks=7;
		
		switch(marks) {
		case 40:
			System.out.println("40 marks");
		break;
		
		case 60:
			System.out.println("60 marks");
		break;
		
		case 75:
			System.out.println("75 marks");
		break;
		 
		default:
			System.out.println("failed");
			
		}
	}

}
